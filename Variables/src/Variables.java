
public class Variables {

	public static void main(String[] args) {
		// 8 tipos de datos primitivos
		
		//Enteros
			byte n1 = 10; //'11111111' -128 a 127
			System.out.println("La variable n1 es : "+n1);

			short n2 = 30; //Miles
			//System.out.println(n2);
			System.out.println("La variable n2 es : "+n2);

			int edad = 22; //Miles de millones
			//System.out.println(edad);
			System.out.println("Mi edad es : "+edad);

			long b3 = 1231231; //Miles de billones
			//System.out.println(b3);
			System.out.println("La variable b3 es : "+b3);
		
		//Punto decimal o Reales
			float area = 16; // 32 bits
			System.out.println("La variable area es : "+area);
			
			double perimetro = 28; // 64 bits
			System.out.println("La variable perimetro es : "+perimetro);
			
		//Char
			char letra = 98;
			System.out.println(letra);
		
		//Boolean 
			boolean x = true;
			boolean y = false;
			System.out.println(x);
		
	}

}
