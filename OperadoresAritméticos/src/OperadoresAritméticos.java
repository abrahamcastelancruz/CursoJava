
public class OperadoresAritméticos {

	public static void main(String[] args) {
		int n1 = 33;
		int n2 = 35;
		
		// Operadores aritméticos
		
			// Suma 
		int resultado = n1 + n2;
		System.out.println(resultado);
			
			//Resta
		resultado = n1 - n2;
		System.out.println(resultado);
		
			//Multiplicación
		resultado = n1 * n2;
		System.out.println(resultado);
		
			//División
		double r2 = n1 / n2;
		System.out.println(r2);
			
			//Modulo
		resultado = n1%2;
		System.out.println(resultado);
		
			//Incremento
		n1++;
		n1++;
		System.out.println(n1);
		
			//Decremento
		n1--;
		n1--;
		System.out.println(n1);
	}

}
